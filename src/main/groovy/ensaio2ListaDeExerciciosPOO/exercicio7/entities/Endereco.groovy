package ensaio2ListaDeExerciciosPOO.exercicio7.entities;

public class Endereco {
  int x
  int y

  Endereco(int x, int y) {
    this.x = x
    this.y = y
  }

  int getX() {
    return x
  }

  void setX(int x) {
    this.x = x
  }

  int getY() {
    return y
  }

  void setY(int y) {
    this.y = y
  }
}
